# What Is The List, How Will You Reverse A List?
# In Python, a built-in function called reverse() is used to reverse the list. This simple and quick way to reverse a list in Python requires little memory.
lst = [1,10,100,1000,100000,100000]
lst.reverse()
print("The Reversed List Is:--> ", lst)

print("Your List Is:-->", list(reversed(lst)))
