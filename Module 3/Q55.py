# # What Is Lambda Function In Python? What We call  a function Which  Is Incomplete Version Of A Function?
# In Python, a lambda function is a small anonymous function that can have any number of arguments, but can only have one expression. It is a way to create a function without giving it a name. 

# A lambda function is defined using the keyword "lambda" followed by the arguments and the expression. For example, a lambda function that returns the square of a number can be defined as follows:

# ```
# square = lambda x: x**2
# ```

# This creates a lambda function that takes one argument "x" and returns the square of "x". The function can be called like any other function:

# ```
# result = square(5)
# print(result) # Output: 25
# ```

# In general, we call a lambda function an incomplete version of a function because it only contains one expression and cannot perform any complex operations or have a block of code. It is useful for simple operations that can be written in one line of code.