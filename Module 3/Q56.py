# # How Many Bascic Types Of Function Are Available in python?
# In Python, there are six basic types of functions:

# 1. Built-in functions: These are functions that come with Python and are available for use without any import statement. Examples include `print()`, `input()`, `len()`, etc.

# 2. User-defined functions: These are functions that are created by the user to perform specific tasks. They are defined using the `def` keyword followed by the function name, parameters, and the function body.

# 3. Recursive functions: These are functions that call themselves recursively until a certain condition is met. They are useful for solving problems that can be broken down into smaller sub-problems.

# 4. Anonymous functions (lambda functions): These are small, one-line functions that do not have a name and are defined using the `lambda` keyword. They are typically used for simple operations that can be written in one line of code.

# 5. Higher-order functions: These are functions that take other functions as arguments or return functions as their output. They are useful for creating more complex functions and for implementing functional programming concepts.

