# # Explain Exception handling ? What Is An Error in python
# Exception handling is the process of catching and dealing with errors that occur during the execution of a program. In Python, exceptions are raised when an error occurs during the execution of a program, and they can be caught and handled using try-except blocks.

# An error in Python is an unexpected or undesired event that occurs during the execution of a program. There are three types of errors in Python:

# 1. Syntax errors: These occur when the code is not written correctly, such as missing a colon or parentheses.

# 2. Runtime errors: These occur when the code is syntactically correct but cannot be executed due to an unforeseen event, such as dividing by zero.

# 3. Logical errors: These occur when the code is executed but produces the wrong results due to a mistake in the logic of the program.