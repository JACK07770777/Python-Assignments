# # What Is The OOPS concepts?  Is multiple inheritance Supported in Java
# # OOPS (Object Oriented Programming System) is a programming paradigm that is based on the concept of objects. It is a way of programming that focuses on creating objects that have their own behavior, state, and identity. The main OOPS concepts are:

# 1. Encapsulation: This is the process of wrapping data and functions into a single unit called a class.

# 2. Inheritance: This is the process of creating new classes from existing ones by inheriting their properties and methods.

# 3. Polymorphism: This is the ability of objects to take on many forms or behave differently in different contexts.

# 4. Abstraction: This is the process of hiding complexity and presenting only essential features to the user.

# Java supports multiple inheritance through the use of interfaces, but not through classes. This means that a class can implement multiple interfaces, but it can only inherit from one class.