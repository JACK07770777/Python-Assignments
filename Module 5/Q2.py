# To check the installed version of Django:

# 1. Open the command prompt or terminal on your computer.
# 2. Type the following command, and press Enter:

#        python -m django --version

# 3. The output will show the installed version of Django on your system. For example, if you have Django version 3.1.5 installed, the output will look like this:

#        3.1.5

# Alternatively, you can also check the installed version of Django from within your Django project's Python shell. To do this, open the command prompt or terminal and type the following commands:

# 1. Start the Python shell by typing `python` and pressing Enter.
# 2. Type the following command, and press Enter:

#        import django
       
# 3. Then type:

#        django.VERSION

# 4. The output will show the version of Django installed in your project. For example, if you have Django version 3.1.5 installed, the output will look like this:

#        (3, 1, 5, 'final', 0)

